void main() {
  // Data Types:
  // Numbers, Strings, Booleans, Lists, Maps, Dynamic.

  // Strings:
  String stringExample = "Hello, world!";
  print("String Example: $stringExample");

  // Integers:
  int integerExample = 42;
  print("Integer Example: $integerExample");

  // Doubles:
  double doubleExample = 3.14;
  print("Double Example: $doubleExample");

  // Booleans:
  bool booleanExample = true;
  print("Boolean Example: $booleanExample");

  // Dynamic:
  dynamic dynamicExample = "I can be anything!";
  print("Dynamic Example: $dynamicExample");

  dynamicExample = 42;
  print("Dynamic Example: $dynamicExample");

  dynamicExample = {'key': 'value'};
  print("Dynamic Example: $dynamicExample");
}
