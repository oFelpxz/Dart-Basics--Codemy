void main(){
  // Como declarar as variáveis:

  // Strings
  var name1 = "John";
  String name2 = "Cleber";

  // Int
  var x = 32;
  int y = 12;

  // Dynamic:
  dynamic firstName = "Tim";

  // Const
  const String fullName = "Cleber Bambam"; // Tempo de compilação.
  final String nickName = "Jay"; // Tempo de execução.

  // Declarar e não utilizar.
  var myName;
  myName = "John";

  print(myName);
}